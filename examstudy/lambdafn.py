
area_rectangle = lambda length, width: length * width
area_square = lambda side: side ** 2
area_circle = lambda radius: 3.14159 * (radius ** 2)


print("Area of rectangle (length=5, width=3):", area_rectangle(5, 3))
print("Area of square (side=4):", area_square(4))
print("Area of circle (radius=7):", area_circle(7))
